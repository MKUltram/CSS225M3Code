# This program will compute the area of a circle.


import math

# Prompt the user to enter a Radius.
Radius = float(input("Enter the Radius Of the circle:"))
area_of_the_circle = math.pi*Radius*Radius  # Calculates the radius of circle using import math.
print("The area of the circle is:",area_of_the_circle) # Prints the Radius of the circle.

